#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";

const context = JSON.parse(fs.readFileSync(process.env.HARNESS_PRESET_CONTEXT, "utf8"));
const harnessCore = await import(context.runtime?.coreModule || new URL("../../../dist/harness-core.mjs", import.meta.url).href);
const { normalizeTarget, collectTasks: collectCoreTasks } = harnessCore;

const release = safeToken(context.inputs.release);
const fromVersion = safeToken(context.inputs.from);
const toRevision = String(context.inputs.to || "HEAD").trim() || "HEAD";
if (!release) {
  console.error("publish-standard requires inputs.release");
  process.exit(2);
}
if (!fromVersion) {
  console.error("publish-standard requires inputs.from");
  process.exit(2);
}

const paths = context.paths || {};
if (!paths.tasksRoot || !paths.governanceRoot) {
  console.error("publish-standard requires structure-aware context.paths from the preset runner");
  process.exit(2);
}

const outputRoot = path.join(context.outputRoot, "publish-standard");
fs.mkdirSync(outputRoot, { recursive: true });

const target = normalizeTarget(context.targetRoot);
const allTasks = collectCoreTasks(target)
  .map((task) => publishTaskFromCore(target, task))
  .filter((task) => task.id !== context.task.id && task.shortId !== context.task.id && task.preset !== "publish-standard")
  .sort((a, b) => a.id.localeCompare(b.id));

let selection;
try {
  selection = selectTasks(allTasks, context.inputs, release);
} catch (error) {
  console.error(error.message);
  process.exit(2);
}

const tasks = selection.tasks.sort((a, b) => a.id.localeCompare(b.id));
if (tasks.length === 0) {
  console.error("publish-standard selector matched no tasks");
  process.exit(2);
}

const destinationRoot = `${paths.governanceRoot}/releases/${release}`;
const aggregate = {
  schemaVersion: "publish-standard-aggregate/v1",
  release,
  from: fromVersion,
  to: toRevision,
  generatedAt: new Date().toISOString(),
  selector: selection.selector,
  summary: {
    totalTasks: tasks.length,
    doneTasks: tasks.filter((task) => task.state === "done").length,
    reviewTasks: tasks.filter((task) => /review/i.test(`${task.title} ${task.evidenceSnippet}`)).length,
    dashboardTasks: tasks.filter((task) => /dashboard/i.test(`${task.title} ${task.evidenceSnippet}`)).length,
  },
  matched: tasks.map(taskSummary),
  excluded: selection.excluded.map((task) => ({ ...taskSummary(task), reason: "not-selected" })),
  missing: selection.missing,
};

const publicChangelog = redactPublic(buildPublicChangelog({ release, fromVersion, tasks }));
const publicRedactionReport = {
  schemaVersion: "public-redaction-report/v1",
  status: leaksInternalPublicTerms(publicChangelog) || leaksLocalPath(publicChangelog) ? "fail" : "pass",
  rules: ["internal-implementation-jargon", "local-absolute-paths", "file-urls"],
  checkedFiles: ["public-changelog.md"],
  findings: [
    ...(leaksInternalPublicTerms(publicChangelog) ? ["public changelog contains internal implementation jargon"] : []),
    ...(leaksLocalPath(publicChangelog) ? ["public changelog contains local path"] : []),
  ],
  generatedAt: new Date().toISOString(),
};
if (publicRedactionReport.status !== "pass") {
  console.error("publish-standard public changelog redaction failed");
  process.exit(3);
}

const technicalSummary = buildTechnicalSummary({ release, fromVersion, toRevision, tasks, aggregate });
const publishChecklist = buildPublishChecklist({ release, fromVersion, toRevision });
const packReport = {
  schemaVersion: "publish-standard-pack-report/v1",
  release,
  from: fromVersion,
  to: toRevision,
  status: "pending-owner-run",
  requiredCommands: ["npm run check", "npm run prepublishOnly", "npm run pack:dry-run"],
  forbiddenPaths: [".harness-private/", "AGENTS.md", "CLAUDE.md", "docs/"],
  notes: [
    "Attach npm pack --dry-run --json output before publish.",
    "This preset records readiness material and does not execute npm publish.",
  ],
  generatedAt: new Date().toISOString(),
};
const index = `# Publish Standard Package: ${release}

Generated by \`presets/publish-standard\` through the generic preset runner.

| Document | Purpose |
| --- | --- |
| \`public-changelog.md\` | APP-style user-facing update notes grouped by functional impact. |
| \`technical-summary.md\` | Traceable internal task evidence for release owner review. |
| \`publish-checklist.md\` | npm package gates and owner publish boundary. |
| \`pack-report.json\` | Pack evidence placeholder and forbidden path policy. |
| \`public-redaction-report.json\` | Redaction evidence for public changelog wording. |

## Summary

- Release: ${release}
- From: ${fromVersion}
- To: ${toRevision}
- Selected tasks: ${tasks.length}
- Publish boundary: this preset does not execute \`npm publish\`.
`;

write("INDEX.md", index);
write("public-changelog.md", publicChangelog);
write("technical-summary.md", technicalSummary);
write("publish-checklist.md", publishChecklist);
write("pack-report.json", `${JSON.stringify(packReport, null, 2)}\n`);
write("task-aggregate.json", `${JSON.stringify(aggregate, null, 2)}\n`);
write("public-redaction-report.json", `${JSON.stringify(publicRedactionReport, null, 2)}\n`);

fs.writeFileSync(context.materializationManifestPath, `${JSON.stringify({
  schemaVersion: "preset-materialization/v1",
  status: "ok",
  publicRedactionReport: { source: "publish-standard/public-redaction-report.json" },
  writes: [
    { source: "publish-standard/INDEX.md", destination: `${destinationRoot}/INDEX.md`, type: "text" },
    { source: "publish-standard/public-changelog.md", destination: `${destinationRoot}/public-changelog.md`, type: "text", visibility: "public" },
    { source: "publish-standard/technical-summary.md", destination: `${destinationRoot}/technical-summary.md`, type: "text" },
    { source: "publish-standard/publish-checklist.md", destination: `${destinationRoot}/publish-checklist.md`, type: "text" },
    { source: "publish-standard/pack-report.json", destination: `${destinationRoot}/pack-report.json`, type: "json" },
    { source: "publish-standard/task-aggregate.json", destination: `${destinationRoot}/task-aggregate.json`, type: "json" },
    { source: "publish-standard/public-redaction-report.json", destination: `${destinationRoot}/public-redaction-report.json`, type: "json" },
  ],
}, null, 2)}\n`);

function write(name, content) {
  fs.writeFileSync(path.join(outputRoot, name), content.endsWith("\n") ? content : `${content}\n`);
}

function buildPublicChangelog({ release, fromVersion, tasks }) {
  const items = tasks.map(publicItem);
  const additions = items.filter((item) => /新增|add|create|introduce|support|dashboard|关联|relation/i.test(item.raw));
  const improvements = items.filter((item) => /优化|improve|upgrade|flow|readiness|search|确认|review/i.test(item.raw));
  const fixes = items.filter((item) => /修复|fix|remove|fallback|cleanup|block|问题/i.test(item.raw));
  const stability = items.filter((item) => /稳定|reliab|audit|gate|check|一致|队列|queue/i.test(item.raw));
  const docs = items.filter((item) => /文档|模板|preset|publish|release|changelog|docs|template/i.test(item.raw));
  return `# ${release} 更新说明

本次更新覆盖 ${fromVersion} 到 ${release} 的发布窗口，重点是让任务状态、人工确认、发布材料和用户可读更新说明更清晰。

## 功能新增

${formatPublicItems(additions, "本版本没有单独归类的新增功能。")}

## 功能优化

${formatPublicItems(improvements, "本版本没有单独归类的功能优化。")}

## 问题修复

${formatPublicItems(fixes, "本版本没有单独归类的问题修复。")}

## 稳定性提升

${formatPublicItems(stability, "本版本没有单独归类的稳定性提升。")}

## 文档与模板更新

${formatPublicItems(docs, "本版本没有单独归类的文档或模板更新。")}

## 重要说明

- 发布规范只准备更新说明、技术摘要和 npm package 检查材料，不会自动执行发布。
- 用户可见说明会优先保留 Dashboard、Human Review、任务关联等功能语义，内部实现细节保留在技术摘要中。
`;
}

function publicItem(task) {
  const text = sanitizePublic(`${task.title}${task.evidenceSnippet ? `：${task.evidenceSnippet}` : ""}`);
  return {
    raw: `${task.title} ${task.evidenceSnippet}`,
    text,
  };
}

function formatPublicItems(items, emptyText) {
  const seen = new Set();
  const lines = [];
  for (const item of items) {
    const text = item.text.trim();
    if (!text || seen.has(text)) continue;
    seen.add(text);
    lines.push(`- ${text}`);
  }
  return lines.length ? lines.join("\n") : `- ${emptyText}`;
}

function sanitizePublic(value) {
  return redactPublic(String(value || "")
    .replace(/projection-first/gi, "任务状态一致性")
    .replace(/facade/gi, "内部兼容层")
    .replace(/route through repository/gi, "统一任务数据入口")
    .replace(/fallback/gi, "兼容路径")
    .replace(/\s+/g, " ")
    .trim());
}

function buildTechnicalSummary({ release, fromVersion, toRevision, tasks, aggregate }) {
  return `# Publish Standard Technical Summary: ${release}

## Version Window

- From: ${fromVersion}
- To: ${toRevision}
- Selected tasks: ${tasks.length}

## Selector

\`\`\`json
${JSON.stringify(aggregate.selector, null, 2)}
\`\`\`

## Task Evidence

${tasks.map((task) => `- ${task.id}
  - Title: ${task.title}
  - State: ${task.state}
  - Preset: ${task.preset || "none"}
  - Evidence: ${task.evidenceSnippet || "n/a"}`).join("\n")}
`;
}

function buildPublishChecklist({ release, fromVersion, toRevision }) {
  return `# Publish Checklist: ${release}

## Version Window

- From: ${fromVersion}
- To: ${toRevision}

## Required Gates

1. Run \`npm run check\`.
2. Run \`npm run prepublishOnly\`.
3. Run \`npm run pack:dry-run\`.
4. Inspect pack output for forbidden paths: \`.harness-private/\`, \`AGENTS.md\`, \`CLAUDE.md\`, and root \`docs/\`.
5. Confirm npm registry state before publish.

## Owner Boundary

This preset prepares and checks publish materials. It does not execute \`npm publish\`; the package owner performs publish after reviewing the generated files and command evidence.
`;
}

function selectTasks(tasks, inputs, release) {
  const hasTaskList = Boolean(inputs.taskList);
  const hasTaskQuery = Boolean(String(inputs.taskQuery || "").trim());
  if (!hasTaskList && !hasTaskQuery) throw new Error("publish-standard requires --task-list or --task-query");
  if (hasTaskList && hasTaskQuery) throw new Error("publish-standard accepts only one selector: --task-list or --task-query");

  if (hasTaskList) {
    const taskList = inputs.taskList;
    if (!taskList || typeof taskList !== "object" || Array.isArray(taskList)) {
      throw new Error("--task-list must be a JSON object");
    }
    if (taskList.schemaVersion !== "publish-standard-task-list/v1") {
      throw new Error("--task-list schemaVersion must be publish-standard-task-list/v1");
    }
    if (taskList.release && taskList.release !== release) {
      throw new Error(`--task-list release ${taskList.release} does not match --release ${release}`);
    }
    const requestedTaskIds = Array.isArray(taskList.taskIds) ? taskList.taskIds.map(normalizeTaskListId).filter(Boolean) : [];
    if (requestedTaskIds.length === 0) throw new Error("--task-list requires non-empty taskIds");
    const selected = resolveTaskReferences(requestedTaskIds, tasks);
    const selectedIds = new Set(selected.map((task) => task.id));
    return {
      selector: {
        type: "task-list",
        schemaVersion: taskList.schemaVersion,
        release,
        sourcePath: taskList.sourcePath || "",
        taskIds: requestedTaskIds,
        resolvedTaskIds: selected.map((task) => task.id),
      },
      tasks: selected,
      excluded: tasks.filter((task) => !selectedIds.has(task.id)),
      missing: [],
    };
  }

  const query = String(inputs.taskQuery || "").trim();
  const terms = parseTaskQuery(query);
  const selected = tasks.filter((task) => terms.every((term) => matchesQueryTerm(task, term)));
  const selectedIds = new Set(selected.map((task) => task.id));
  return {
    selector: {
      type: "task-query",
      query,
      terms,
    },
    tasks: selected,
    excluded: tasks.filter((task) => !selectedIds.has(task.id)),
    missing: [],
  };
}

function normalizeTaskListId(value) {
  return String(value || "").trim();
}

function resolveTaskReferences(references, tasks) {
  return references.map((reference) => {
    const canonical = tasks.filter((task) => task.id === reference);
    const candidates = canonical.length > 0 ? canonical : tasks.filter((task) => taskReferenceMatches(task, reference));
    if (candidates.length === 0) {
      throw new Error(`Missing task reference: ${reference}`);
    }
    if (candidates.length > 1) {
      throw new Error(`Ambiguous task reference: ${reference} matched ${candidates.map((task) => task.id).join(", ")}`);
    }
    return candidates[0];
  });
}

function taskReferenceMatches(task, reference) {
  if (/^(TASKS|MODULES|EXTERNAL)\//.test(reference)) return task.id === reference;
  return task.shortId === reference || task.legacyId === reference || task.id.endsWith(`/${reference}`);
}

function parseTaskQuery(query) {
  const rawTerms = query.split(/\s+/).map((item) => item.trim()).filter(Boolean);
  if (rawTerms.length === 0) throw new Error("--task-query requires at least one selector term");
  return rawTerms.map((term) => {
    let match = term.match(/^date:(\d{4}-\d{2}-\d{2})\.\.(\d{4}-\d{2}-\d{2})$/);
    if (match) return { type: "date", from: match[1], to: match[2] };
    match = term.match(/^state:([A-Za-z0-9_-]+)$/);
    if (match) return { type: "state", value: normalizeState(match[1]) };
    match = term.match(/^preset:([A-Za-z0-9._-]+)$/);
    if (match) return { type: "preset", value: match[1].toLowerCase() };
    match = term.match(/^module:([A-Za-z0-9._-]+)$/);
    if (match) return { type: "module", value: match[1].toLowerCase() };
    throw new Error(`Unsupported --task-query term: ${term}`);
  });
}

function matchesQueryTerm(task, term) {
  if (term.type === "date") {
    const date = task.date || "";
    return date >= term.from && date <= term.to;
  }
  if (term.type === "state") return task.state === term.value;
  if (term.type === "preset") return (task.preset || "none") === term.value;
  if (term.type === "module") return (task.module || "").toLowerCase() === term.value;
  return false;
}

function taskSummary(task) {
  return {
    id: task.id,
    title: task.title,
    state: task.state,
    preset: task.preset || "none",
    module: task.module || "",
    shortId: task.shortId || "",
  };
}

function publishTaskFromCore(target, task) {
  const shortId = task.shortId || task.id.split("/").at(-1) || "";
  return {
    ...task,
    preset: task.taskPreset || "none",
    date: shortId.match(/^(\d{4}-\d{2}-\d{2})/)?.[1] || "",
    evidenceSnippet: readEvidenceSnippet(target, task),
  };
}

function readEvidenceSnippet(target, task) {
  const taskPath = task.path || task.localPath || "";
  if (!taskPath) return "";
  const projectRoot = target.projectRoot || target.root || "";
  const absolute = path.isAbsolute(taskPath) ? taskPath : path.join(projectRoot, taskPath.replace(/^TARGET:/, ""));
  for (const name of ["brief.md", "progress.md", "task_plan.md"]) {
    const file = path.join(absolute, name);
    if (!fs.existsSync(file)) continue;
    const content = fs.readFileSync(file, "utf8");
    const line = content.split(/\r?\n/).map((item) => item.trim()).find((item) => item && !item.startsWith("#") && !item.startsWith("|") && !item.startsWith("Task Contract:"));
    if (line) return line.replace(/^[-*]\s*/, "").slice(0, 240);
  }
  return "";
}

function normalizeState(value) {
  const raw = String(value || "").trim().toLowerCase().replaceAll("-", "_").replaceAll(" ", "_");
  return ["not_started", "planned", "in_progress", "review", "blocked", "done"].includes(raw) ? raw : "unknown";
}

function safeToken(value) {
  const token = String(value || "").trim();
  return /^[A-Za-z0-9][A-Za-z0-9._-]{0,63}$/.test(token) ? token : "";
}

function redactPublic(value) {
  return String(value || "")
    .replace(/file:\/\/\/[^\s)"'`<>\]]+/g, "LOCAL_FILE_URL_REDACTED")
    .replaceAll("file://", "LOCAL_FILE_URL_REDACTED")
    .replace(/\/Users\/[^/\s)"'`<>\]]+(?:\/[^\s)"'`<>\]]*)*/g, "LOCAL_PATH_REDACTED")
    .replace(/\/Volumes\/[^\s)"'`<>\]]+(?:\/[^\s)"'`<>\]]*)*/g, "LOCAL_PATH_REDACTED")
    .replace(/\/(?:private\/)?tmp\/[^\s)"'`<>\]]+(?:\/[^\s)"'`<>\]]*)*/g, "LOCAL_PATH_REDACTED")
    .replace(/\/var\/folders\/[^\s)"'`<>\]]+(?:\/[^\s)"'`<>\]]*)*/g, "LOCAL_PATH_REDACTED")
    .replace(/\/home\/[^/\s)"'`<>\]]+(?:\/[^\s)"'`<>\]]*)*/g, "LOCAL_PATH_REDACTED")
    .replace(/[A-Za-z]:\\[^\s)"'`<>\]]+(?:\\[^\s)"'`<>\]]*)*/g, "LOCAL_PATH_REDACTED");
}

function leaksInternalPublicTerms(value) {
  return /projection-first|facade|route through repository/i.test(String(value || ""));
}

function leaksLocalPath(value) {
  return /(?:^|[\s"'(])(?:\/Users\/|\/Volumes\/|\/tmp\/|\/private\/tmp\/|\/var\/folders\/|\/home\/|[A-Za-z]:\\|file:\/\/)/.test(String(value || ""));
}
